/**
 * Generates a random character ID using the adjective-adjective-noun pattern
 * with two distinct adjective lists and optional "of the [noun]" suffix
 * @returns {string} A random character ID
 */
export const generateCharacterId = (): string => {
  const adj1 =
    firstAdjectives[Math.floor(Math.random() * firstAdjectives.length)];
  const adj2 =
    secondAdjectives[Math.floor(Math.random() * secondAdjectives.length)];
  const mainNoun = nouns[Math.floor(Math.random() * nouns.length)];

  // 30% chance to add "of the [noun]" suffix
  if (Math.random() < 0.3) {
    const suffixNoun =
      ofTheNouns[Math.floor(Math.random() * ofTheNouns.length)];
    return `${adj1}-${adj2}-${mainNoun}-of-the-${suffixNoun}`;
  }

  return `${adj1}-${adj2}-${mainNoun}`;
};
